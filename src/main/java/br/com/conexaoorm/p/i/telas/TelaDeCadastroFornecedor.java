    private void btnCadastrarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarFornecedorActionPerformed

        if (inputNomeEmpresa.getText().isEmpty() || inputTelefone.getText().isEmpty() || inputEmail.getText().isEmpty() || inputEndereco.getText().isEmpty() || inputCnpj.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!!!");
            return;
        }

        try {

            FornecedorDAO fornecedorDAO = new FornecedorDAO();
            ProdutoDAO produtoDAO = new ProdutoDAO();

            String nomeEmpresa = inputNomeEmpresa.getText();
            String Telefone = inputTelefone.getText();
            String email = inputEmail.getText();
            String endereco = inputEndereco.getText();
            String cnpj = inputCnpj.getText();
            Produto produtoSelecionado = null;
            if (comboProdutos.getSelectedItem() != null) {
                Produto produto = (Produto) comboProdutos.getSelectedItem();
            }
            
            fornecedorDAO.cadastrarFornecedor(nomeEmpresa, Telefone, email, endereco, cnpj, produtoSelecionado);

            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblFornecedores.getModel();

            inputNomeEmpresa.setText("");
            inputTelefone.setText("");
            inputEmail.setText("");
            inputEndereco.setText("");
            inputCnpj.setText("");           

            JOptionPane.showMessageDialog(null, "Fornecedor cadastrado com sucesso!");
            
            carregarFornecedores();

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, "Falha ao cadastrar fornecedor!!! " + ex.getMessage());

        }

    }//GEN-LAST:event_btnCadastrarFornecedorActionPerformed

