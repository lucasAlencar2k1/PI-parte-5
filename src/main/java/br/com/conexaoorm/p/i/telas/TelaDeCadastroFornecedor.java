    public void listarProdutosCadastrado() {

        try {

            ProdutoDAO produtoDAO = new ProdutoDAO();

            List<Produto> listaDeProdutos = produtoDAO.listarProdutos();

            for (Produto produtos : listaDeProdutos) {
                comboProdutos.addItem(produtos);
            }

        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(null, "Falha ao carregar lista de produtos!!!");
            
        }

    }
    
    public void carregarFornecedores() {
        
        try {
            
            FornecedorDAO fornecedorDAO = new FornecedorDAO();
            
            List<Fornecedor> listaDeFornecedores = fornecedorDAO.listaFornecedores();
            
            DefaultTableModel modelo = (DefaultTableModel) tblFornecedores.getModel();
            
            modelo.setRowCount(0);
            
            for (Fornecedor fornecedor : listaDeFornecedores) {
                
                List<String> nomesProduto = new ArrayList<>();
                
                for (Produto produto : fornecedor.getProdutos()) {
                    nomesProduto.add(produto.getNome());
                }
                
                String nomeProduto = String.join(", ", nomesProduto);
                
                if (nomesProduto.isEmpty()) {
                    nomeProduto = "Sem fornecedor";
                }
                
                modelo.addRow(new Object[] {
                    fornecedor.getId(),
                    fornecedor.getNomeEmpresa(),
                    fornecedor.getTelefone(),
                    fornecedor.getEmail(),   
                    fornecedor.getEndereco(),
                    fornecedor.getCnpj(),
                    nomeProduto
                });
            }
            
        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(null, "Falha ao carregar lista de fornecedores!!!");
            
        }
        
    }

