    private void carregaListaFornecedores() {

        try {

            FornecedorDAO fornecedorDAO = new FornecedorDAO();

            List<Fornecedor> listaDeFornecedores = fornecedorDAO.listaFornecedores();

            for (Fornecedor fornecedor : listaDeFornecedores) {
                comboFornecedor.addItem(fornecedor);
            }

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, "Falha ao carregar lista de fornecedores!!!");

        }

    }

    public void carregarlistarProdutos() {

        try {

            DefaultTableModel modelo = (DefaultTableModel) tblProdutos.getModel();
            modelo.setRowCount(0);
            
            ProdutoDAO produtoDAO = new ProdutoDAO();
            List<Produto> listaDeProdutos = produtoDAO.listarProdutos();           

            for (Produto produto : listaDeProdutos) {
                
                List<String> nomesFornecedores = new ArrayList<>();
                for (Fornecedor fornecedor : produto.getFornecedores()) {
                    nomesFornecedores.add(fornecedor.getNomeEmpresa());
                }
                
                String fornecedoresTexto = String.join(", ", nomesFornecedores);                
                
                if (fornecedoresTexto.isEmpty()) {
                    fornecedoresTexto = "Sem fornecedor";
                }
                                
                modelo.addRow(new Object[]{
                    produto.getId(),
                    produto.getNome(),
                    produto.getQuantidade(),
                    produto.getPreco(),
                    produto.getDescricao(),
                    produto.getDataDeCadastro(),                    
                    fornecedoresTexto
                });
            }

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, "Falha ao carregar lista de produtos!!!");

        }

    }

