    private void btnCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProdutoActionPerformed

        if (inputNome.getText().isEmpty() || inputQuantidade.getText().isEmpty() || inputPreco.getText().isEmpty() || inputDescricao.getText().isEmpty() || inputData.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!!!");
            return;
        }

        String nome = inputNome.getText();
        int quantidade = Integer.parseInt(inputQuantidade.getText());
        BigDecimal preco = new BigDecimal(inputPreco.getText().trim().replace(",", "."));
        String descricao = inputDescricao.getText();
        LocalDate dataDeCadastro = LocalDate.now();                
        Fornecedor fornecedorSelecionado = null;
        if (comboFornecedor.getSelectedItem() != null)  {
            fornecedorSelecionado = (Fornecedor) comboFornecedor.getSelectedItem();
        }

        try {

            String precoTexto = inputPreco.getText().trim().replace(",", ".");
            ProdutoDAO produtoDAO = new ProdutoDAO();
            produtoDAO.cadastrarProduto(nome, quantidade, preco, descricao, dataDeCadastro, fornecedorSelecionado);

            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!");

            carregarlistarProdutos();

            inputNome.setText("");
            inputQuantidade.setText("");
            inputPreco.setText("");
            inputDescricao.setText("");
            inputData.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro: preencha a Quantidade e o Preço com valores numéricos válidos.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar produto: " + ex.getMessage());
        }

    }//GEN-LAST:event_btnCadastrarProdutoActionPerformed

