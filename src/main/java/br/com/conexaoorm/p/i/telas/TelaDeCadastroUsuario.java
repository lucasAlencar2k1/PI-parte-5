    public void carregarUsuarios() {

        try {

            UsuarioDAO usuarioDAO = new UsuarioDAO();

            List<Usuario> listaDeUsuarios = usuarioDAO.listaUsuarios();

            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblUsuarios.getModel();

            modelo.setRowCount(0);

            for (Usuario usuario : listaDeUsuarios) {
                modelo.addRow(new Object[]{
                    usuario.getId(),
                    usuario.getNome(),
                    usuario.getSenha(),
                    usuario.getEmail(),
                    usuario.getRole()
                });
            }

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, "Falha ao carregar lista de usuarios!!!");

        }

    }

