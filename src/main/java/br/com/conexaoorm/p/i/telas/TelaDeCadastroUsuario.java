    private void btnCadastrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarUsuarioActionPerformed

        UsuarioDAO usuarioDAO = new UsuarioDAO();

        if (inputNome.getText().isEmpty() || inputSenha.getText().isEmpty() || inputEmail.getText().isEmpty() || inputCargo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!!!");
            return;
        }

        try {

            String nome = inputNome.getText();
            String senha = inputSenha.getText();
            String email = inputEmail.getText();
            String cargo = inputCargo.getText();

            usuarioDAO.cadastrarUsuario(nome, senha, email, cargo);

            JOptionPane.showMessageDialog(null, "Usuário cadastrado com sucesso!");
            
            carregarUsuarios();
            
            inputNome.setText("");
            inputSenha.setText("");
            inputEmail.setText("");
            inputCargo.setText("");           

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, "Falha ao cadastrar usuário!!! " + ex.getMessage());

        }

    }//GEN-LAST:event_btnCadastrarUsuarioActionPerformed

